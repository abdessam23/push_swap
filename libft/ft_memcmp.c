#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const unsigned char		*d1;
	const unsigned char		*d2;
	size_t					i;

	i = 0;
	d1 = (const unsigned char *) s1;
	d2 = (const unsigned char *) s2;
	while (i < n)
	{
		if (d1[i] != d2[i])
			return (d1[i] - d2[i]);
		i++;
	}
	return (0);
}
